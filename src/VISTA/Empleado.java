    private void btnagregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnagregarActionPerformed

        Connection con = null;

        try {
            con = getConection();
            CallableStatement CST = con.prepareCall("{call insertar (?,?,?,?,?,?,?,?)}");
            CST.setString(1, txtid.getText());
            CST.setString(2, txtnombre.getText());
            CST.setString(3, txtapellido.getText());
            CST.setString(4, txtnit.getText());
            CST.setString(5, txtdireccion.getText());
            CST.setString(6, activo.getSelectedItem().toString());
            CST.setDate(7, Date.valueOf(txtfecha.getText()));
            CST.setString(8, txtsalario.getText());

            int res = CST.executeUpdate();
          
            if (res > 0) {
                JOptionPane.showMessageDialog(null, "empleado guardado");

            } else {
                JOptionPane.showMessageDialog(null, "No se pudo almacenar empleado");

            }

            con.close();

        } catch (Exception e) {
            System.err.println(e);
        }

        String Dato[] = new String[8];
        Dato[0] = txtid.getText();
        Dato[1] = txtnombre.getText();
        Dato[2] = txtapellido.getText();
        Dato[3] = txtnit.getText();
        Dato[4] = txtdireccion.getText();
        Dato[5] = (String) activo.getSelectedItem();
        Dato[6] = txtfecha.getText();
        Dato[7] = txtsalario.getText();

        //agregamos filas con el objeto modelo
        modelo.addRow(Dato);
    }//GEN-LAST:event_btnagregarActionPerformed

    private void btnmodificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnmodificarActionPerformed

        Connection con = null;

        try {
            con = getConection();
            CallableStatement CST = (CallableStatement) con.prepareCall("UPDATE empleado SET ID_EMPLEADO=?, NOMBRE=?, APELLIDO=?, NIT=?,DIRECCION=?,ACTIVO=?, FECHA_INGRESO=?, SUELDO=? WHERE ID_EMPLEADO=?");
            CST.setString(1, txtid.getText());
            CST.setString(2, txtnombre.getText());
            CST.setString(3, txtapellido.getText());
            CST.setString(4, txtnit.getText());
            CST.setString(5, txtdireccion.getText());
            CST.setString(6, activo.getSelectedItem().toString());
            CST.setDate(7, Date.valueOf(txtfecha.getText()));
            CST.setString(8, txtsalario.getText());
            CST.setString(9, txtbuscar.getText());
            
            int res = CST.executeUpdate();

            if (res > 0) {
                JOptionPane.showMessageDialog(null, "exito en la modificacion");
                limpiarCajas();
            } else {
                JOptionPane.showMessageDialog(null, "no se pudo modificar");
                limpiarCajas();
            }

            con.close();

        } catch (Exception e) {
            System.err.println(e);
        }

        // al seleccionar la fila ya seleccionada, se modificara segun la codicion
        int FilaSelec = tabla.getSelectedRow();
        //si >0 es porque hay un registro si no dara error, 
        if (FilaSelec >= 0) {
            //fila y coluna, lo que va en cada columna se ira modificando
            txtid.setText(tabla.getValueAt(FilaSelec, 0).toString());
            txtnombre.setText(tabla.getValueAt(FilaSelec, 1).toString());
            txtapellido.setText(tabla.getValueAt(FilaSelec, 2).toString());
            txtnit.setText(tabla.getValueAt(FilaSelec, 3).toString());
            txtdireccion.setText(tabla.getValueAt(FilaSelec, 4).toString());
            activo.setSelectedItem(tabla.getValueAt(FilaSelec, 5).toString());
            txtfecha.setText(tabla.getValueAt(FilaSelec, 6).toString());
            txtsalario.setText(tabla.getValueAt(FilaSelec, 7).toString());
            modelo.removeRow(FilaSelec);
        } else {
            JOptionPane.showMessageDialog(this, "no se pudo moificar en tabla");
        }
    }//GEN-LAST:event_btnmodificarActionPerformed

