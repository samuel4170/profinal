    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed

        
        Connection con = null;
        
        try{
        con = getConection();
        CallableStatement CST=(CallableStatement)con.prepareCall("DELETE FROM empleado WHERE ID_EMPLEADO=?");
        CST.setString(1,txtid.getText());
        
        int res = CST.executeUpdate();
        //mayor a un regisro
        if(res > 0){
            JOptionPane.showMessageDialog(null, "exito en la eliminacion");
            limpiarCajas();
        }
        else
        {
            JOptionPane.showMessageDialog(null,"no se pudo eliminar");
            limpiarCajas();
        }
        
        con.close();
        
        }catch(Exception e){
        System.err.println(e);
        }


        int FilaSelec = tabla.getSelectedRow();
        if (FilaSelec > 0) {
            modelo.removeRow(FilaSelec);
        } else {
            JOptionPane.showMessageDialog(this, "no se puede eliminar, no exisen empleados en la tabla");
        }
    }//GEN-LAST:event_btneliminarActionPerformed

